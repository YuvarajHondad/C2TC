package venu;
import java.util.*;

public class square 
{
	public static void main(String[] args)
	{
		Scanner sc = new Scanner(System.in);
		System.out.println("Enter the number of rows");
		int rows = sc.nextInt();
		int i=1;
		
		while(i<= rows)
		{
			System.out.println("* * * *");
		    i++;
		}
	}

}
